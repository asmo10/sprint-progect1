package day49;

public class Book {

	
	protected String author;
	int pageCount;
	double price;
	int chapterCount;
	String title;
	@Override
	public String toString() {
		return "Book [author=" + author + ", pageCount=" + pageCount + ", price=" + price + ", chapterCount="
				+ chapterCount + ", title=" + title + "]";
	}
	
	
	
	
	
	

}
